     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $unas = Unas::find($request->unas_id);
        Cart::add(
            $unas->id,
            $unas->nombre,
            $unas->precio,
            1,
            array("urlfoto"=>$unas->urlfoto)
        );
        return back()->with('success',"$unas->nombre ¡se ha agregado con éxito al carrito!");
    }

